import math
#importing with allies For example : import math as m
#also to import specific libraries in python type "from math import sqrt,pow"

#complement operator (~). also known as bitwise NOT operator
# It returns 1's complement of a number  
print(~15)

#Xor 
print(12^13)

print(math.floor(2.9))
print(math.ceil(2.9))

#power function

print(math.pow(3,2))
print(3 ** 2)
print(3 ** 3)


#& operator
q=12
w=20
print(q & w)
print(bin(q & w))


# | (OR) operator
a = 11
b = 19
print(a | b)
print(bin(a | b))


#<< left shift operator
n=5
print(n)
print(n<<1)
print(n<<2)


#>> right shift operator
m=5
print(m>>1)
