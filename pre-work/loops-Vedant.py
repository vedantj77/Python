#there are 2 types of loops in python for loop and while loop


#ascending order 

i = 1
while i<=5 :
    print('bocha' , i)
    i=i+1


#descending order 

j=5
while j>=1:
    print('gandu', j)
    j=j-1



#for loop 
#we can use this when we want to print all values from the list
x = ['navin',33,44.5]

for i in range(len(x)):
    print(i)



